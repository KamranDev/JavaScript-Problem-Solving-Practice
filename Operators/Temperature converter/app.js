function cToF() 
{
  const cTemp = 45;
  let cToFahr = cTemp * 9 / 5 + 32;
  //let message = cTemp+'\xB0C is ' + cToFahr + ' \xB0F.';
    console.log(cToFahr);
}

function fToC() 
{
  const fTemp = 30;
  let fToCel = (fTemp - 32) * 5 / 9;
  //let  message = fTemp+'\xB0F is ' + fToCel + '\xB0C.';
    console.log(fToCel);
} 